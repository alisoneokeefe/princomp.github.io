class ClassRoom
{
    private string building;
    private int number;

    public void SetBuilding(string buildingParam)
    {
        building = buildingParam;
    }

    public string GetBuilding()
    {
        return building;
    }

    public void SetNumber(int numberParam)
    {
        number = numberParam;
    }

    public int GetNumber()
    {
        return number;
    }
}
